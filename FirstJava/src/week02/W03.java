package week02;

public class W03 {
    //비교연산자
    public static void main(String[] args) {
        System.out.println(10 > 9); //t
        System.out.println(10 >= 9); //t
        System.out.println(10 < 9); //f
        System.out.println(10 <= 9); //f
        System.out.println(10 == 10); //t
        System.out.println(10 == 9); //f
        System.out.println(10 != 10); //f
        System.out.println(10 != 9); //t
    }
}
